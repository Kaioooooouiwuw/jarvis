#  JARVIS GESTURE SYSTEM v4.1 — Controle por Gestos + Pinça + Punho
# ═══════════════════════════════════════════════════════════════
#
#  Submódulos:
#    voice_control         → Ativação/desativação por comando de voz
#    pinch_click           → Detecção de pinça (polegar + indicador) → clique/drag
#    camera                → Captura de vídeo (webcam)
#    hand_tracking         → Rastreamento de mãos (MediaPipe)
#    gesture_recognition   → Classificação de gestos
#    actions               → Automação do sistema (mouse, teclado)
#
#  Gestos:
#    🖐 Mão aberta         → Movimento do cursor
#    👌 OK                  → Modo especial (trocar abas, mover janelas)
#    ☝️ Apontar             → Cursor preciso
#    ✌️ Paz (V)             → Screenshot
#    🤙 Scroll              → Scroll vertical (2 dedos juntos)
#    🤏 Pinça rápida        → Clique simples (abrir e fechar rápido)
#    🤏 Pinça segurando     → Drag and Drop (manter fechada)
#    ✊ Punho               → Drag and Drop (alternativa à pinça)
#
#  RESTAURADO na v4.1:
#    ✊ Punho               → De volta como alternativa para Drag and Drop
#
#  Ativação por voz:
#    "Jarvis, ative o controle de gesto"
#    "Jarvis, desative o controle de gesto"
